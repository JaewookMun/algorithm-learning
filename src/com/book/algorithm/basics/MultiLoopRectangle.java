package com.book.algorithm.basics;

// Q14. 정사각형
public class MultiLoopRectangle {
    public static void main(String[] args) {
        for(int i=0; i<5; i++){
            for(int j=0; j<5; j++){
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
